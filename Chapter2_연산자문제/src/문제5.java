
public class 문제5 {
	public static void main(String[] arg) 
	{
	      int num1=10;   
	      System.out.printf("%d \n", num1);
	      num1++;
	      System.out.printf("%d \n", num1);
	      ++num1;
	      System.out.printf("%d \n", num1);
	      --num1;
	      System.out.printf("%d \n", num1);
	      num1--;
	      System.out.printf("%d \n", num1);
	}
	/*
	 * 정답
	 * ---------------------
	 * 10
	 * 11
	 * 12
	 * 11
	 * 10
	 */
}
