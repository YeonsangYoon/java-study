// & | ^
/*             &      |      ^
 *    ---------------------------
 *     00      0      0      0 
 *    ---------------------------
 *     01      1      1      1
 *    ---------------------------
 *     10      0      1      1
 *    ---------------------------
 *     11      1      1      0
 *    ---------------------------
 *    
 *    
 *    8 & 3 = 0
 *     1000
 *     0011
 *     ----&
 *     0000
 *    
 *    8 | 3 = 11
 *     1000
 *     0011
 *     ----|
 *     1011
 *     
 *    8 ^ 3 = 11
 *     1000
 *     0011
 *     ----^
 *     1011
 *     
 *    13 & 6 = 4
 *     1101
 *     0110
 *     ----&
 *     0100
 */
public class 비트연산자 {
	public static void main(String[] arg) {
		System.out.println(8&3);
		System.out.println(8|3);
		System.out.println(8^3);
		System.out.println(13&6);
	}
}
